package com.rafa.recycleview.Alo.Model;

public class Model {


    private String infoo;
    private String inp;


    public Model(String infoo, String inp) {
        this.infoo = infoo;
        this.inp = inp;
    }

    public String getInfoo() {
        return infoo;
    }

    public void setInfoo(String infoo) {
        this.infoo = infoo;
    }

    public String getInp() {
        return inp;
    }

    public void setInp(String inp) {
        this.inp = inp;
    }
}
